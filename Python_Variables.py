# Integer variable.
age = 17
print("Age: ", age)

#Float variable.
Temperature = 36.6
print("Temperature: ", Temperature)

#String variable.
Name = "Alice"
print("Name: ", Name)

# Boolean variable.
isStudent = True
print("isStudent: ", isStudent)

# List variable
Fruits = ["Apple", "Banana", "Kiwi", "Watermelon"]
print("Fruits: ", Fruits)

# Tuple variable
Coordinates = (10, 20)
print("Coordinates: ", Coordinates)

#Dictionary variables
person = {"Name": "John", "Age": 18}
print("Person: ", person)

# Reassigning a variable
age = 19
print(age)