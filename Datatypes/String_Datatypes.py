Name = "Alice"
greeting = "Hello"
multiline = '''This is a multi line string
(also known as multiline comment)'''
print(type(Name))
print(type(greeting))
print(type(multiline))

# Some basic operations on the string.
text = "Python Programming language."
print(text.upper()) # Converts to the uppercase.
print(text.lower()) # Converts to the lowercase.
print(text[0]) # Access the character at the index 0.
print(text[1:4]) # Slice the string from index 1 to 3.
print(len(text)) # Get the length of the string.
