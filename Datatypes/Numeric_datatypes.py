# Python supports three types of the numbes:-
#int:- Integer
#float:- Floating point value(i.e.:- decimal)
#complex:- Complex number with real and imaginary parts.

x = 10
print(f"Integer x is = {x}.")

y = 3.14
print(f"Floating point value is:- {y}.")

z = 2 + 3j
print(f"Complex number is:- {z}.")

# To check the datatypes of these x, y, z
print(type(x))
print(type(y))
print(type(z))